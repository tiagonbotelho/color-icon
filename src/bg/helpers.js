function colorToHex(color) {
  switch(color) {
    case "red": return "#FB635E";
    case "green": return "#86E26D";
    case "blue": return "#53BDF6";
    case "yellow": return "#FDD74F";
    case "orange": return "#FCAB47";
    case "purple": return "#D58FE3";
  }
}

function tabKey(tab) {
  return tab.id.toString();
}
